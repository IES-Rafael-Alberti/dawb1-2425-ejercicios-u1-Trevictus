"""Escribir un programa que muestre por pantalla el resultado de la siguiente operación aritmética
((3+2)/(2*5))*((3+2)/(2*5))
"""

def operacionAritmetica():
    operacion = ((3+2)/(2*5))*((3+2)/(2*5))

    print(f"La operacion da de resultado: {operacion:.2f}")

operacionAritmetica()